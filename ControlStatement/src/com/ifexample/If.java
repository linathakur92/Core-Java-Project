package com.ifexample;

public class If {
   static   int p =40;
   static  int q=50;
 // if condition is true then control goes into curly bracess if condition is not true then control goes to else statement and Execute    
    public static void main(String args []) {
    	 if (p<q) {
    		 System.out.println(" p is less than q ");
    	 }
    }
}
